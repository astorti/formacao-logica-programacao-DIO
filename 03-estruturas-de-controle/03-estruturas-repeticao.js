// FOR
console.log("FOR")
for(let contador = 0; contador < 4; contador++) {
    console.log(contador)
}

console.log("-------------")
for (let contador = 1; contador <= 4; contador++) {
    console.log(contador)
}

console.log("-------------")

// WHILE
console.log("WHILE")
let contador = 0
while(contador <= 3) {
    console.log(contador)
    contador++
}

console.log("-------------")

// DO-WHILE
console.log("DO-WHILE")
contador = 0
do {
    console.log(contador)
    contador++
} while (contador <=3)